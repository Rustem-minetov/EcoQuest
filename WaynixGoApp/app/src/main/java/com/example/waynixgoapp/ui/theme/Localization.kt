val LocalWaynixStrings = compositionLocalOf { RuStrings }

@Composable
fun ProvideWaynixStrings(languageCode: String, content: @Composable () -> Unit) {
    val strings = when (languageCode.uppercase()) {
        "UZ" -> UzStrings
        else -> RuStrings
    }
    CompositionLocalProvider(LocalWaynixStrings provides strings) {
        content()
    }
}
